// Create a dynamic variable and assign it a string value. Then assign it a number. Print the
// variable after each assignment.

void main() {
  dynamic text = 'Omnia';
  print('my name is $text');

  text = 21;
  print('I am $text years old');

  //we declared variable with dynamic data type so we can assign any type of value to it as it is weak data type
}
