// Declare a var variable and assign a string value to it. Then reassign an integer to the same
// variable. Print the variable after each assignment

void main() {
  var text;
  //we declared variable with var key word without assigning any value to it so it is declared as dynamic data type

  text = 'Omnia';
  print('my name is $text');
  //so here in a new line we can assigne String value to the variable as it was declared as dynamic

  text = 21;
  print('I am $text years old');
  //and here in a new line we can assigne int value to the variable as it was declared as dynamic
}
